fn main() {
    //addition
    let sum:u32 = 5550 + 7310;
    println!("5550 + 7310 = {}", sum);

    //subtraction
    let difference:f32 = 95.5 - 4.3;
    println!("95.5 - 4.3 = {}", difference);

    //multiplication
    let product :u32 = 4 * 30;
    println!("4 * 30 = {}", product);

    //division
    let quotient:f64 = 56.7 / 32.2;
    println!("56.7 / 32.2 = {}", quotient);

    //remainder
    let remainder = 43 % 5;
    println!("the remainder of 43 when divided by 5 = {}",remainder );
}
